<script setup lang="ts">
import { ref } from "vue";

// Define reactive data and props

const greeting = ref("Hello, Vue 3!");
const counter = ref<number>(0);

// Define a function
function incrementCounter() {
  counter.value++;
}
</script>

<template>
  <div>
    <h1>{{ greeting }}</h1>
    <button @click="incrementCounter">
      Click me!
    </button>
    <p>Counter: {{ counter }}</p>
  </div>
</template>

